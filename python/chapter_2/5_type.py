a = 10
print(type(a))
# <class 'int'>


b = 10.8
print(type(b))
# <class 'float'>


c = "Kishore"
print(type(c))
# <class 'str'>


d = None
print(type(d))
# <class 'NoneType'>

e = 6 + 7j
print(type(e))
# <class 'complex'>


f = True
print(type(f))
# <class 'bool'>


g = type(f)
print(type(g))
# <class 'type'>